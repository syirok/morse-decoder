const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};

function decode(expr) {

    let str = "";
    let code = "";
    for (let i=0; i<=expr.length-10;  i+=10) {
        if (expr.slice(i,i+10) == "**********") {str+=" ";}
        else {
            for (let j=i; j<=i+9; j+=2) {
                if (expr.slice(j,j+2) == "10") {code += ".";}
                if (expr.slice(j,j+2) == "11") {code += "-";}
            }
            str += MORSE_TABLE[code];
        }
        code = "";
    }
    return str;
}

module.exports = {
    decode
}